public class Variables {
    public static void main(String[] args) {
        int x; // Declaration
		x = 6; // Initialization
		System.out.println(x); // Without Quitation marks ke x ki value print hogi.
		System.out.println("x"); // Quitation mark ke beech me text print hoga as it is.
		int y = 8; // Declaration and Initialization
		System.out.println(y);
		System.out.println(x+y);
    }
    
}
