public class Arithematicoperationondoubledatatype {
    public static void main(String[] args) {
        double x = 5.0, y = 2;
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y); // int*int = int,
		                           // int*float = float
		                           //float*float= float
		System.out.println(x / y);
    }
    
}
